# Functions - collection of lines of code that perform a certain task
# starts with `def` keyword
# then has the name of the function
# list of parameters/arguments
# the lines of code

# Parameters - variables defined in the function
# Arguments - actual values passed to the function

def greet(fname, lname):
    print(f"Hello {fname} {lname}")

# execute/call the function
greet("John", "Smith")

greet("First", "Last")